/**
 * Helios is a Micro validation library
 *
 * Helios is a micro validation library. Most of Java validation
 * libraries out there are based on annotations. Annotations are ok,
 * but then validation depends on having your data model polluted with
 * them. Moreover, sometimes you may want to have different validation
 * restrictions over the same object in different parts of the
 * application.
 *
 * Principal ideas in `Helios` development are:
 *
 * - API simplicity
 * - Footprint
 * - Validator reusability
 * - Documentation
 *
 * @since 0.1.0
 */
